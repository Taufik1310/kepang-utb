export function GetTopman() {
  return MANAGEMENT.filter(
    (item) => item.category === "TOPMAN" && item.variant === "V1",
  );
}

export function GetTopmanV2() {
  return MANAGEMENT.filter(
    (item) =>
      item.category === "TOPMAN" &&
      (item.variant === "V2" || item.variantAlt === "V2"),
  );
}

export function GetDivisions() {
  return MANAGEMENT.filter((item) => item.category === "DIVISI");
}

